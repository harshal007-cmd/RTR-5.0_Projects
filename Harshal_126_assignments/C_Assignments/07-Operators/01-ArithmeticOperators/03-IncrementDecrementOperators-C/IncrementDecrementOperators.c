#include<stdio.h>

int main()
{
	int hvs_a = 10;
	int hvs_b = 5;

	printf("\n\n");

	
	printf("hvs_a = %d\n", hvs_a);
	printf("hvs_a = %d\n", hvs_a++);
	printf("hvs_a = %d\n", hvs_a);
	printf("hvs_a = %d\n", ++hvs_a);
	printf("\n");
	printf("hvs_b = %d\n", hvs_b);
	printf("hvs_b = %d\n", hvs_b--);
	printf("hvs_b = %d\n", hvs_b);
	printf("hvs_b = %d\n", --hvs_b);
	
	printf("\n\n");
	return 0;
}
